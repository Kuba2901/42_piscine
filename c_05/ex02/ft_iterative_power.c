int	ft_iterative_power(int nb, int power)
{
	int	num;

	num = nb;
	if (nb == 0 && power == 0)
		return (1);
	if (power == 0)
		return (1);
	if (power < 0)
		return (0);
	while (power > 1)
	{
		num *= nb;
		power--;
	}
	return (num);
}
