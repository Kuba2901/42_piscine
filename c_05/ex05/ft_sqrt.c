int	ft_sqrt(int nb)
{
	int	i;

	i = 2;
	if (nb == 0)
		return (0);
	if (nb == 1)
		return (1);
	while (i < nb / 2 + 1)
	{
		if (i * i == nb)
			return (i);
		if (i * i > nb)
			return (0);
		i++;
	}
	return (0);
}
